void printImage (int[,] matrix){
    for(int i = 0; i < matrix.GetLength(0); i++){
        for(int j = 0; j < matrix.GetLength(1); j++){
            if(matrix[i,j] > 4) matrix[i,j] = 1;

            if(matrix[i,j] == 1)
                //System.Console.Write("█▓▒░"); 
                System.Console.Write("█"); 
            else if(matrix[i,j] == 2)
                System.Console.Write("▓"); 
            else if(matrix[i,j] == 3)
                System.Console.Write("▒"); 
            else if(matrix[i,j] == 4)
                System.Console.Write("░"); 
            else System.Console.Write(" "); 

        }
        System.Console.WriteLine();
    }
}

void fillImage(int[,] matrix, int x, int y, int color, int colorToRepace){
    if(x<0 || x >= matrix.GetLength(0)){return;}
    if(y<0 || y >= matrix.GetLength(1)){return;}

    if (colorToRepace < 0){colorToRepace = matrix[x,y];}

    if(matrix[x,y]==colorToRepace){
        matrix[x,y] = color;
        fillImage(matrix,x-1, y, color, colorToRepace);
        fillImage(matrix,x, y-1, color, colorToRepace);
        fillImage(matrix,x+1, y, color, colorToRepace);
        fillImage(matrix,x, y+1, color, colorToRepace);
    }
}

